SELECT
  ID, EESNIMI, PERENIMI,
  (MATEMAATIKA + VOORKEEL + EMAKEEL) AS TULEMUS
FROM LEPIKULT.KANDIDAADID
WHERE (id like '3%' or ID like '5%')
  AND (MATEMAATIKA + VOORKEEL+ EMAKEEL) < 150;

